function giveSelection1(){
 
            var list1 = document.getElementById('firstList');
            var list2 = document.getElementById("secondList");
            var list1SelectedValue = list1.options[list1.selectedIndex].value;
             
            if (list1SelectedValue=='Gold')
            {
                 
                list2.options.length=0;
                list2.options[0] = new Option('--Select--', '');
                list2.options[0].disabled = true;
                list2.options[1] = new Option('24K Gold', '24K Gold');
                list2.options[2] = new Option('14K Gold', '14K Gold');
                
                 
            }
            else if (list1SelectedValue=='Silver')
            {
                 
                list2.options.length=0;
                list2.options[0] = new Option('--Select--', '');
                list2.options[0].disabled = true;
                list2.options[1] = new Option('Silver Plates', 'Silver Plates');
                list2.options[2] = new Option('Silver-filled', 'Silver-filled');
                
                 
            }
}

